/**
 * Match the channel's own gameplay videos to blog posts and emit
 * src/data/blog/videoMap.ts  (slug -> { id, title }).
 *
 * Video source = the optimizer's pre-run backups in scripts/youtube/backups/*.json
 * (each is [{id,title,description}]). Merged + deduped → widest local catalog.
 * (When OAuth quota is free, run audit-descriptions first to widen the catalog.)
 *
 * Matching is HIGH PRECISION on purpose — a wrong video on a post hurts trust.
 * We only map when every subject token from the slug appears in a video title
 * alongside a guide-ish word, plus a small set of hand-curated exact matches.
 *
 * Run: node scripts/youtube/match-videos-to-posts.js
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..', '..');
const BDIR = path.join(__dirname, 'backups');
const OUT = path.join(ROOT, 'src', 'data', 'blog', 'videoMap.ts');

const vids = {};
for (const f of fs.readdirSync(BDIR).filter(f => f.endsWith('.json'))) {
  try {
    const arr = JSON.parse(fs.readFileSync(path.join(BDIR, f), 'utf8'));
    for (const v of arr) if (v.id && v.title) vids[v.id] = v.title;
  } catch {}
}
const vidArr = Object.entries(vids).map(([id, title]) => ({ id, title }));
const norm = s => s.toLowerCase().replace(/[^a-z0-9 ]/g, ' ').replace(/\s+/g, ' ').trim();

// Hand-curated exact matches for high-traffic evergreen posts (verified titles).
const CURATED = {
  'cookie-run-kingdom-tier-list': /\btier list\b/,
  'cookie-run-kingdom-reroll-guide': /\breroll\b/,
};
// Titles that mean the cookie is the ENEMY/mode, not the build subject — never a build match.
const BOSS_CTX = /(beat|boss|stage|rush|trial|raid|abyss|viewing|gacha|alliance|guild battle|destiny's flight|cake tower|tray)/;
// Canonical "this video IS that cookie's build/gear guide" patterns.
const BUILD_CTX = /(gear guide|cookie build|gear & toppings|gear and toppings|best gear|toppings? (guide|build))/;

const STOP = new Set(['cookie','toppings','build','guide','best','crk','ranked','the','and','to','for','in','of','a','an','run','kingdom','your','how','what','cards','card']);
function subjectTokens(slug) {
  return slug
    .replace(/-cookie-toppings-build|-cookie-toppings|-toppings-build|-toppings|-crk-ranked|-crk|-build|-guide/g, '')
    .split('-').filter(w => w && !STOP.has(w));
}

function readPosts(game) {
  const dir = path.join(ROOT, 'src', 'data', 'blog', game, 'posts');
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir).filter(f => f.endsWith('.ts')).map(f => {
    const src = fs.readFileSync(path.join(dir, f), 'utf8').split(/\n\s*content:/)[0];
    const m = src.match(/\n\s*slug:\s*['"`]([^'"`]+)['"`]/);
    const t = src.match(/\n\s*title:\s*['"`]([^'"`]+)['"`]/);
    return { slug: m && m[1], title: t && t[1] };
  }).filter(p => p.slug);
}

const map = {};
const games = ['cookie-run-kingdom', 'cookie-run-braverse-tcg'];
for (const game of games) {
  for (const p of readPosts(game)) {
    // 1) curated evergreen posts
    const cur = CURATED[p.slug];
    if (cur) {
      const v = vidArr.find(v => cur.test(norm(v.title)));
      if (v) { map[p.slug] = { id: v.id, title: v.title }; continue; }
    }
    // 2) cookie/card build posts: require ALL subject tokens + a BUILD context, and
    //    reject boss/mode titles where the subject is the enemy, not the build. Pick best.
    const subj = subjectTokens(p.slug);
    if (!subj.length) continue;
    const cands = vidArr.filter(v => {
      const nt = norm(v.title);
      return subj.every(tok => nt.includes(tok)) && BUILD_CTX.test(nt) && !BOSS_CTX.test(nt);
    });
    if (!cands.length) continue;
    // Prefer the canonical "<X> Cookie Gear Guide" phrasing.
    cands.sort((a, b) => (/gear guide/.test(norm(b.title)) ? 1 : 0) - (/gear guide/.test(norm(a.title)) ? 1 : 0));
    map[p.slug] = { id: cands[0].id, title: cands[0].title };
  }
}

const header = `// AUTO-GENERATED by scripts/youtube/match-videos-to-posts.js — do not hand-edit.\n// Maps a blog post slug to the channel's own gameplay video (first-hand media / E-E-A-T).\n// Rendered as an embedded player + VideoObject JSON-LD on the post.\nexport type PostVideo = { id: string; title: string };\nexport const VIDEO_MAP: Record<string, PostVideo> = ${JSON.stringify(map, null, 2)};\n`;
fs.writeFileSync(OUT, header, 'utf8');
console.log(`videos in local catalog: ${vidArr.length}`);
console.log(`matched ${Object.keys(map).length} posts -> wrote ${path.relative(ROOT, OUT)}`);
for (const [slug, v] of Object.entries(map)) console.log(`  ${slug}\n     -> ${v.id}  "${v.title}"`);
